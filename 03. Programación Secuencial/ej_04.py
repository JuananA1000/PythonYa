'''
   Escribir un programa en el cual se ingresen cuatro números, calcular e informar la suma de los dos primeros y el
   producto del tercero y el cuarto.  
'''

num1 = int(input('Introduce número 1: '))
num2 = int(input('Introduce número 2: '))
num3 = int(input('Introduce número 3: '))
num4 = int(input('Introduce número 4: '))

print('NUM 1 + NUM 2: ', num1 + num2)
print('NUM 3 * NUM 4: ', num3 * num4)
