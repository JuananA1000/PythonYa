'''
  Inicializar un string con la cadena "mAriA" luego llamar a sus métodos upper(), lower() y capitalize(), guardar los 
  datos retornados en otros string y mostrarlos por pantalla.
'''

cadena = 'mAriA'

en_MAYUSCULAS = cadena.upper()
en_minusculas = cadena.lower()
en_Capital = cadena.capitalize()

print('En mayúsculas: ', en_MAYUSCULAS)
print('En minúsculas: ', en_minusculas)
print('La primera en mayúsculas: ', en_Capital)
